// Types of variables on JavaScript
// --------------------------------

// <---- Primitive types ---->

// String is a text variable
let text = "Example text";

// Numeric variables (integer/decimal)
let number = 25;

// True or false value
let isTrue = false;

// Undefined is a declared variable without value
let undefined;

// Null is an intentionally null variable
let isNull = null;

// Symbol represents a variable with a unique id
Symbol("id");

// BigInt represents an integer number that is really big (bigger than Number.MAX_SAFE_INTEGER)
let bigInt = 12345678901234567890n;