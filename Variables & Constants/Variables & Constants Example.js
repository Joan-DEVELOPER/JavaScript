// Example of variables & Constants in JavaScript
// ----------------------------------------------
// A variable is a piece of your device memory, user to store temporary information.
// Variables can be set using three reserved words (var, let, const)

// First we have var, witch is the old way of declaring variables in JavaScript. 
// Nowadays, its rarely used, so its highly recomended to use let instead
var String = "Hello World";

// The modern way of declaring variables in JavaScript, is using let or const.
// let lets you create a variable that can be changes during script execution.
let int = 0;

// const its a type of variable that whose value can't be changed.
const message = "JavaScript is fun"